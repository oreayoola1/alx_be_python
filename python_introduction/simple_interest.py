# simple_interest.py

# This program calculates simple interest

# Step 1: Set the principal amount
principal = 1000

# Step 2: Set the interest rate (5% = 0.05)
rate = 0.05

# Step 3: Set the time in years
time = 3

# Step 4: Calculate the simple interest
interest = principal * rate * time

# Step 5: Print the result
print("The simple interest is:", interest)

